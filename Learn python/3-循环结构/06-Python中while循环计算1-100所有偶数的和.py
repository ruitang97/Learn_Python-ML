'''
案例1：使用while循环求1..100中所有偶数的和
能被2整除的数就是偶数（整数）  =>  if 数字 % 2 == 0:
2、4、6、8、10...
while循环三步走 + if判断结构
'''
# 第一步：初始化计数器
i = 1
sum = 0
# 第二步：编写循环条件
while i <= 100:
    # 判断i这边变量是否为偶数
    if i % 2 == 0:
       print(i)
       sum += i
    # 在循环体内部更新计数器
    i += 1
print(sum)


