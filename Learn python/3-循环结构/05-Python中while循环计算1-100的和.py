'''
案例1：使用while循环求1..100的和
'''
# 第一步：定义初始化计数器
i = 1
# 引入一个新的变量，sum
sum = 0
# 第二步：编写循环条件
while i <= 100:
    # 1次循环，sum=0，i=1，sum需要累加1-100，先加1 => sum = 1
    # 2次循环，sum=1，i=2，sum需要累加1-100，先加2 => sum = 1 + 2 = 3   0+1+2
    # ...
    # 100次循环，sum=5050-100, i=100，先加100 => sum = 4950 + 100 = 5050
    sum += i
    # 第三步：在循环体内部对计数器进行更新
    i += 1

print(sum)  # 输出语句写在循环体外侧，代表当循环结束后，一共只需要输出1次即可