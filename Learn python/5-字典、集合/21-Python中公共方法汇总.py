'''
+ : 把两个序列进行合并
* : 对序列中的数据进行复制操作
'''
str1 = 'hello'
str2 = 'world'
print(str1 + str2)

list1 = [1, 2, 3]
list2 = [4, 5]
print(list1 + list2)

tuple1 = (1, 2, 3)
tuple2 = (4, 5)
print(tuple1 + tuple2)

str3 = '-'
print(str3 * 20)

list3 = [10]
print(list3 * 3)

tuple3 = (20,)
print(tuple3 * 2)