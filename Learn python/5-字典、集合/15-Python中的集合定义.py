# 1、定义一个集合类型
set1 = {10, 20, 30, 50, 60, 15}
print(type(set1))

# 2、使用set方法定义第一个集合
set2 = set('abcdefg')
print(set2)
print(type(set2))

# 3、天生去重
set3 = {10, 20, 30, 20, 40, 50}
print(set3)

# 4、如何定义一个空集合
set4 = set()
print(set4)
print(type(set4))

# 5、不能使用一对花括号来定义空集合
set5 = {}
print(type(set5))


